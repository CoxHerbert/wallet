<template>
	<div class="wf-switch">
                <van-switch
                        v-model="checked"
                        :active-value="activeValue"
                        :inactive-value="inactiveValue"
                        size="20"
                        :disabled="disabled"
                        @change="onChange"
                ></van-switch>
	</div>
</template>

<script>
import Props from '../../mixins/props.js'
export default {
	name: 'wf-switch',
	mixins: [Props],
	watch: {
		text: {
			handler(val) {
				this.initValue()
				this.handleChange(val)
			}
		},
	},
	data() {
		return { checked: false, activeValue: '1', inactiveValue: '0' }
	},
	methods: {
		initValue() {
			const { dicData } = this.column
			if (dicData && dicData.length == 2) {
				this.activeValue = dicData[1].value
				this.inactiveValue = dicData[0].value
			}
			this.checked = this.text == this.activeValue
		},
		onChange(val) {
			this.text = val
		}
	}
}
</script>

<style lang="scss" scoped>
.wf-switch {
	display: flex;
	align-items: center;
}
</style>
